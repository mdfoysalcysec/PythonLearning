#If we want to convert number Like- 
'''
Decimal to Binary 
Binary to Octal
Octal to Hexa or something like that we can do it also
'''
a  = 15 # Decimal value 
print(a)

b = bin(a)#Now it will show the binary value of a
print(b)

c = oct(a)#Now it will show the octal value of a
print(c)

d = hex(a)#Now it will show the octal value of a
print(d)